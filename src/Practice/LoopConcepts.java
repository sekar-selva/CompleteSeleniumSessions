package Practice;

public class LoopConcepts {

	public static void main(String[] args) {
		
		for(int a =0;a<=10;a++)
		System.out.print(a+",");
		
		for(char b= 'a';b<='z';b++)
		{
			//System.out.println();
			System.out.println("a to z");
			System.out.println(b);
		}
		for(char c = 'A'; c<='Z'; c++)
		{
			System.out.println(c);
		}
		
		for(char d = 'a'; d<='z';d++)
		{
			System.out.println((d+ " : " +(int)d));
		}
	}
}
